using UnityEngine;

public class PlayerOxygen : MonoBehaviour
{
    public float maxOxygen = 100f; //* максимум кислорода
    public float oxygenDrainRate = 5f; //* скорость расхода в секунду

    private float currentOxygen; //* текущий уровень кислорода

    private PlayerHealth playerHealth; //* ссылка на компонент PlayerHealth, чтобы наносить урон при нехватке кислорода

    void Start() //! Инициализируем текущий уровень кислорода при старте игры и получаем ссылку на компонент PlayerHealth
    {
        currentOxygen = maxOxygen; //? Инициализируем текущий уровень кислорода при старте игры, устанавливая его равным максимальному
        playerHealth = GetComponent<PlayerHealth>(); //? Получаем ссылку на компонент PlayerHealth, который находится на том же объекте, что и этот скрипт, чтобы мы могли наносить урон игроку, когда кислород закончится
    }

    void Update() //! В каждом кадре вызываем метод DrainOxygen, который будет уменьшать уровень кислорода и наносить урон, если кислород закончится
    {
        DrainOxygen(); //? Вызываем метод DrainOxygen в каждом кадре, чтобы он уменьшал уровень кислорода и наносил урон игроку, если кислород закончится
    }

    void DrainOxygen() //! Метод для уменьшения уровня кислорода и нанесения урона, если кислород закончится
    {
        if (currentOxygen > 0) //? Если текущий уровень кислорода больше нуля, то мы уменьшаем его на скорость расхода, умноженную на время между кадрами (Time.deltaTime), чтобы обеспечить плавное уменьшение кислорода независимо от частоты кадров
        {
            currentOxygen -= oxygenDrainRate * Time.deltaTime; //? Уменьшаем текущий уровень кислорода на скорость расхода, умноженную на время между кадрами (Time.deltaTime), чтобы обеспечить плавное уменьшение кислорода независимо от частоты кадров
        }
        else
        {
            currentOxygen = 0; //? Если текущий уровень кислорода меньше или равен нулю, то мы устанавливаем его в ноль, чтобы избежать отрицательных значений

            
            playerHealth.TakeDamage(1); //? Наносим урон игроку, вызывая метод TakeDamage из компонента PlayerHealth, передавая ему количество урона (в данном случае 1), когда кислород закончится
        }
    }

    public void AddOxygen(float amount) //! Метод для добавления кислорода игроку, который может быть вызван из других скриптов (например, при сборе кислородного танка)
    {
        currentOxygen += amount; //? Увеличиваем текущий уровень кислорода на переданное количество

        if (currentOxygen > maxOxygen) //? Если текущий уровень кислорода превышает максимальный, то мы устанавливаем его равным максимальному, чтобы избежать превышения лимита кислорода
            currentOxygen = maxOxygen; //? Устанавливаем текущий уровень кислорода равным максимальному, если он превышает его, чтобы избежать превышения лимита кислорода
    }

    public float GetOxygen() //! Метод для получения текущего уровня кислорода, который может быть вызван из других скриптов (например, для отображения в UI)
    {
        return currentOxygen; //? Возвращаем текущий уровень кислорода, чтобы другие скрипты могли получить эту информацию (например, для отображения в UI)
    }

    public float GetMaxOxygen() //! Метод для получения максимального уровня кислорода, который может быть вызван из других скриптов (например, для отображения в UI)
    {
        return maxOxygen; //? Возвращаем максимальный уровень кислорода, чтобы другие скрипты могли получить эту информацию (например, для отображения в UI)
    }
}